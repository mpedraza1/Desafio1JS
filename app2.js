//Solicitamos 2 numeros distintos al usuario
let num1 = parseInt(prompt("Ingrese un numero mayor a cero"))
let num2 = parseInt(prompt("Ingrese otro numero mayor a cero"))

//Hacemos las distintas operaciones
resulSuma= (num1) + (num2)
resulResta= (num1) - (num2)
resulMultip= (num1) * (num2)
resulDiv= (num1) / (num2)
resulMod= (num1) % (num2)

//Mostramos en consola los distintos resultados
console.log(resulSuma)
console.log(resulResta)
console.log(resulMultip)
console.log(resulDiv)
console.log(resulMod)

